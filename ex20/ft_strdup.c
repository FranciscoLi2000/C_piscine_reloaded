#include <unistd.h>
#include <stdlib.h>

char	*ft_strdup(char *src);

int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len] != '\0')
		len++;
	return (len);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int	len;
	char	*dst;

	if (src == 0)
		return (0);
	len = ft_strlen(src) + 1;
	dst = malloc(len * sizeof(char));
	if (dst == 0)
		return (0);
	ft_strcpy(dst, src);
	return (dst);
}
